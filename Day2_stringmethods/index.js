let name = "MEGHANA"
console.log(name.length); //7                         //=>length()

console.log(name.toUpperCase()) //MEGHANA             //=>toUpperCase()

console.log(name.toLowerCase()) //meghana             //=>toLowerCase()

console.log(name[1]) //E                              //=>[]

console.log(name.indexOf("G")) //2                   //=>indexOf()

console.log(name.charAt(4)) //H                      //=>charAt()

console.log(name.includes("H")) //true               //=>includes()

console.log(name.startsWith("M")) //true             //=>startsWith()

console.log(name.endsWith("A")) //true               //=>endsWith()

console.log(name.concat("GURRAM")) //MEGHANAGURRAM   //=>concat()  



